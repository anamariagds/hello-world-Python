# Declara o primeiro número e o atribui a variável valor1
valor1 = 90
# Declara o primeiro número e o atribui a variável valor2
valor2 = 30

# Realiza a divisão inteira de valor1 pelo valor2 e atribui a variável calculo
# Divisão com um barra ( / ) suporta vírgula (divisão com número quebrado/decimal), já a divisão com duas barras ( // ) retorna valores exatos (sem vírgula/numéro inteiro).
calculo = valor1 // valor2

# Imprime a variável calculo
print (calculo)


